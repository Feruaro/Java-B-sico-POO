        int f = sliFim.getValue();
        int c = sliContador.getValue();
        
        DefaultListModel lista = new DefaultListModel();
        
        
        for(int i=in; i<=f; i+=c){
            lista.addElement(i);
        }
        
        lstCont.setModel(lista);
